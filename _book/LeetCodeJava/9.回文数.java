// @lc code=start
class Solution {
    public boolean isPalindrome(int x) {
        // if(x < 0){
        //     return false;
        // }else{
        //     int n = 0;
        //     int temp = x;
        //     while(x != 0){
        //         n = n*10 + x % 10;
        //         x /= 10;
        //     }
        //     return temp == n;
        // }
        if (x < 0) {
            return false;
        }
        int help = 1;
        int tmp = x;
        while (tmp >= 10) {
            help *= 10;
            tmp /= 10;
        }
        while (x != 0) {
            if (x % 10 != x / help) {
                return false;
            }
            x = x % help / 10;
            help /= 100;
        }
        return true;
    }
}
